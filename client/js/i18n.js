export const SUPPORTED_LOCALES = Object.keys(MESSAGES);

let activeLocale = "en";

export function getLocale() {
  return activeLocale;
}

export function setLocale(locale) {
  if (!MESSAGES[locale]) return; // unsupported -- keep the current locale
  activeLocale = locale;
  try {
    localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  } catch {
    // storage unavailable (private mode etc.) -- locale still works for the session
  }
}

/**
 * Resolution order: explicit user choice (localStorage) -> browser language
 * (primary subtag, with nb/nn folded into no) -> en.
 */
export function detectLocale(browserLanguage) {
  try {
    const stored = localStorage.getItem(LOCALE_STORAGE_KEY);
    if (stored && MESSAGES[stored]) return stored;
  } catch {
    // fall through to browser language
  }
  const primary = (browserLanguage || "").toLowerCase().split("-")[0];
  if (primary === "nb" || primary === "nn") return "no";
  return MESSAGES[primary] ? primary : "en";
}

export function t(key, params = {}) {
  const template = MESSAGES[activeLocale][key] ?? MESSAGES.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (match, name) => (name in params ? String(params[name]) : match));
}

/**
 * Applies the active locale to static markup: elements with `data-i18n` get
 * their textContent replaced, `data-i18n-placeholder` sets the placeholder.
 */
export function applyTranslations(doc) {
  for (const element of doc.querySelectorAll("[data-i18n]")) {
    element.textContent = t(element.getAttribute("data-i18n"));
  }
  for (const element of doc.querySelectorAll("[data-i18n-placeholder]")) {
    element.setAttribute("placeholder", t(element.getAttribute("data-i18n-placeholder")));
  }
  // Icon-only controls: localize the tooltip and accessible name, not the content.
  for (const element of doc.querySelectorAll("[data-i18n-title]")) {
    const text = t(element.getAttribute("data-i18n-title"));
    element.setAttribute("title", text);
    element.setAttribute("aria-label", text);
  }
}
